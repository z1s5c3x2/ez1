package 과제.과제10;

public class Member {

   // 1. 멤버변수
   String id;
   String password;
   String name;
   String phone;
   int age;
   
   public Member(String id, String password, String name, String phone, int age) {
      this.id = id;
      this.password = password;
      this.name = name;
      this.phone = phone;
      this.age = age;
   }
   
}